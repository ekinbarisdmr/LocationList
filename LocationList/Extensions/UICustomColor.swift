import Foundation
import UIKit

extension UIColor {

    class func buttonBackgroundColor() -> UIColor {
        
        return UIColor(red: 255/255, green: 177/255, blue: 86/255, alpha: 1.0)
    }
    
    class func viewBackgroundColor() -> UIColor {
        
        return UIColor(red: 30/255, green: 78/255, blue: 95/255, alpha: 1.0)
    }
    
    class func textColor() -> UIColor {
        
        return UIColor(red: 151/255, green: 159/255, blue: 173/255, alpha: 1.0)
    }
    
    class func cellBackgroundColor() -> UIColor {
        
        return UIColor(red: 2/255, green: 22/255, blue: 57/255, alpha: 1.0)
    }
    
}
